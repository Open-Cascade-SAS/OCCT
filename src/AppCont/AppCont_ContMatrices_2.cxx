void MMatrix(const Standard_Integer classe, math_Matrix& M)
{
  if (classe > 24)
    throw Standard_DimensionError("MMatrix: classe > 24");
  Standard_Integer i, j, k = 0, Som = 0;
  for (i = 2; i < classe; i++)
  {
    Som += (i) * (i);
  }
  for (i = 1; i <= classe; i++)
  {
    for (j = 1; j <= classe; j++)
    {
      M(i + M.LowerRow() - 1, j + M.LowerCol() - 1) = BBMatrix[Som + k];
      k++;
    }
  }
}
